package lecture_02_19;

import java.util.Scanner;

public class CountDown {
   public static void main (String [] args) {
      int userNum = 0;
      int i = 0;

      userNum = 4;     // Assume positive

      for (i = 1 ; i <= userNum; );
      {

         System.out.print (i + " ");
      }
      System.out.println("");

      return;
   }
}